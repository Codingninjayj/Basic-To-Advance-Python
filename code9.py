# Example:
frute="harry"
a=len(frute) 
print(a)
print(frute[0:4]) #including 0 but not 4
print(frute[1:4]) #including 0 but not 4
print(frute[:5])  #Slicing from Start
print(frute[:-3]) #Slicing using negative index
print(frute[-1:-3])
print(frute[5:])  
print(frute[-4:-2])